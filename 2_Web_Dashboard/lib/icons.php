}

// Gắn version theo thời điểm sửa file để chống cache CSS/JS cũ sau mỗi lần deploy.
function asset(string $path): string
{
    $full = __DIR__ . '/../' . $path;
    $version = is_file($full) ? filemtime($full) : time();
    return $path . '?v=' . $version;
}
